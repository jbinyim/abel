CREATE DATABASE abel;
USE abel;

CREATE TABLE MEMBER (
	MEMBER_ID		VARCHAR(20) PRIMARY KEY,
    MEMBER_PASSWD	VARCHAR(1000),
    MEMBER_NM		VARCHAR(50),
    EMAIL			VARCHAR(20),
    EMAILSTS_YN 	VARCHAR(20),
	DATE_BIRTH 		VARCHAR(20),
    SEX 			VARCHAR(10),
	HP 				VARCHAR(20),
	SMSSTS_YN 		VARCHAR(20),
	ZIPCODE 		VARCHAR(20),
	ROAD_ADDRESS 	VARCHAR(500),
	JIBUN_ADDRESS 	VARCHAR(500),
	NAMUJI_ADDRESS 	VARCHAR(500)
);

CREATE TABLE BENEFICIARY (
	BENCEFICIARY_CD 		INT AUTO_INCREMENT PRIMARY KEY,
    BENCEFICIARY_NM			VARCHAR(50),
    BENCEFICIARY_COMMENT	VARCHAR(2000),
    CONTRIBUTION			INT,
    BENCEFICIARY_BIRTH		VARCHAR(20),
    BENCEFICIARY_SEX		VARCHAR(10),
    BENCEFICIARY_HOBBY		VARCHAR(2000),
    BENCEFICIARY_FAMILY		VARCHAR(2000),
    BENCEFICIARY_COUNTRY	VARCHAR(50)
);